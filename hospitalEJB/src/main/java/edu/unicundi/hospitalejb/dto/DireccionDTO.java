package edu.unicundi.hospitalejb.dto;

import edu.unicundi.hospitalejb.entity.Medico;
import java.io.Serializable;

/**
 *
 * @author Erika Moreno
 */
class DireccionDTO implements Serializable {
    
   
    private Integer id;
    
    private Medico medico;
    
    private String direccionDetallada;
    
    private String barrio;
    
    private String codigoPostal;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Medico getMedico() {
        return medico;
    }

    public void setMedico(Medico medico) {
        this.medico = medico;
    }

    public String getDireccionDetallada() {
        return direccionDetallada;
    }

    public void setDireccionDetallada(String direccionDetallada) {
        this.direccionDetallada = direccionDetallada;
    }

    public String getBarrio() {
        return barrio;
    }

    public void setBarrio(String barrio) {
        this.barrio = barrio;
    }

    public String getCodigoPostal() {
        return codigoPostal;
    }

    public void setCodigoPostal(String codigoPostal) {
        this.codigoPostal = codigoPostal;
    }
    
    
}
