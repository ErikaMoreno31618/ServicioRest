package edu.unicundi.hospitalejb.dto;

import edu.unicundi.hospitalejb.entity.Consulta;
import edu.unicundi.hospitalejb.entity.Direccion;
import java.io.Serializable;
import java.util.Date;
import java.util.List;

/**
 *
 * @author Erika Moreno
 */
public class MedicoDTO implements Serializable {

    private Integer id;

    private String nombre;

    private String apellido;

    private Date fechaNacimiento;

    private String correo;

    private DireccionDTO direccion;

    private List<ConsultaDTO> consultas;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public Date getFechaNacimiento() {
        return fechaNacimiento;
    }

    public void setFechaNacimiento(Date fechaNacimiento) {
        this.fechaNacimiento = fechaNacimiento;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public DireccionDTO getDireccion() {
        return direccion;
    }

    public void setDireccion(DireccionDTO direccion) {
        this.direccion = direccion;
    }

    public List<ConsultaDTO> getConsultas() {
        return consultas;
    }

    public void setConsultas(List<ConsultaDTO> consultas) {
        this.consultas = consultas;
    }

}
